export const transpose = (array) => {
  var result = [];
  array.forEach(function (a, i, aa) {
    a.forEach(function (b, j, bb) {
      result[j] = result[j] || [];
      result[j][aa.length - i - 1] = b;
    });
  });
  result = result.map(t => Array.from(t, (_, i) => {
    if (!(i in t)) return WALL;
    else return t[i];
  }))
  return result;
}
if (window.innerWidth < window.innerHeight) {
  levels = levels.map(l => {
    l = transpose(l)
    return l
  })
}
export { levels }
export const biggestLevel = levels.reduce((acc, row) => {
  var maxCol = Math.max(...row.map(a => a.length))
  if (acc["row"] < row.length) {
    acc["row"] = row.length
  }
  if (acc["col"] < maxCol) {
    acc["col"] = maxCol
  }
  return acc
}, { row: 0, col: 0 })
export const getLevelDimensions = (level) => {
  return level.reduce((acc, row) => {
    if (acc["col"] < row.length) {
      acc["col"] = row.length
    }
    return acc
  }, { row: level.length, col: 0 })
}
export const multiplier = 21
export const size = {
  height: multiplier * biggestLevel.row,
  width: multiplier * biggestLevel.col,
}
